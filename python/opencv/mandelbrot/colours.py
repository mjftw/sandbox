from random import randint


def _hex_is_valid(hexstr):
    try:
        int(hexstr, 16)
    except ValueError:
        return False

    return len(hexstr) == 6

def _pad_hex(hexstr):
    return hexstr[::-1].zfill(6)[::-1]

class Colour:
    def __init__(self, hexstr, name=None):
        hexstr = _pad_hex(hexstr)

        if not _hex_is_valid(hexstr):
            raise AttributeError(f'Invalid hex: "{hexstr}"')

        self._hexstr = hexstr
        self.name = name or ''


    @property
    def rgb(self):
        return tuple(int(self._hexstr[i:i+2], 16) for i in (0, 2, 4))

    @property
    def hex(self):
        return self._hexstr

